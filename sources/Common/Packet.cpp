///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Vector::Vector(uint s) : size(0), buffer(0)
{
	Alloc(s);
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vector::~Vector()
{
	Free();
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void *Vector::Data()
{
	return buffer;
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
uint Vector::Size()
{
	return size;
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vector::Alloc(uint s)
{
	Free();
	buffer = malloc(s);
	if (buffer)
	{
		size = s;
	}
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vector::Free()
{
	free(buffer);
	size = 0;
}
