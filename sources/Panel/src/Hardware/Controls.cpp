#include "Controls.h"


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool Control::IsDigit() const
{
    return (value >= Control::B_0 && value <= Control::B_9);
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
char Control::ToChar() const
{
    if(IsDigit())
    {
        return (char)((value - Control::B_0) | 0x30);
    }
    else if(value == B_Dot)
    {
        return '.';
    }

    return '\0';
}
