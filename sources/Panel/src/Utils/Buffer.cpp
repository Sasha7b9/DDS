//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Buffer::Buffer(uint8 data0) : data(0), size(0)
{
    data = (uint8 *)malloc(1);
    if(data)
    {
        data[0] = data0;
        size = 1;
    }
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
Buffer::Buffer(uint8 data0, uint8 data1) : data(0), size(0)
{
    data = (uint8 *)malloc(2);
    if(data)
    {
        data[0] = data0;
        data[1] = data1;
        size = 2;
    }
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
Buffer::Buffer(uint8 data0, uint8 data1, uint8 data2) : data(0), size(0)
{
    data = (uint8 *)malloc(3);
    if(data)
    {
        data[0] = data0;
        data[1] = data1;
        data[2] = data2;
        size = 3;
    }
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
Buffer::~Buffer()
{
    free(data);
    data = 0;
    size = 0;
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
int Buffer::Length() const
{
    return size;
}

//----------------------------------------------------------------------------------------------------------------------------------------------------
uint8 *Buffer::Data() const
{
    return data;
}
